# 1 colher de chá de bicarbonato de sódio
# 1/2 colher de chá de sal
# 2 ovos
# 1 xícara de leite
# 1/2 xícara de óleo vegetal
# 2 colheres de chá de extrato de baunilha
# 1 xícara de água fervente
# Modo de preparo:
# Pré-aqueça o forno a 180°C. Unte uma forma de bolo com manteiga e farinha, ou forre-a com papel manteiga.
# Em uma tigela grande, peneire a farinha de trigo, o açúcar, o cacau em pó, o fermento em pó, o bicarbonato de sódio e o sal. Misture bem.
# Em outra tigela, bata os ovos levemente. Adicione o leite, o óleo vegetal e o extrato de baunilha. Misture bem.
# Despeje os ingredientes líquidos na mistura de ingredientes secos e mexa até que tudo esteja bem combinado.
# Adicione a água fervente à massa e misture até que a massa fique homogênea. A massa ficará líquida, mas é normal.
# Despeje a massa na forma preparada e leve ao forno pré-aquecido.
# Asse por cerca de 30-35 minutos, ou até que um palito inserido no centro do bolo saia limpo.
# Retire o bolo do forno e deixe esfriar na forma por alguns minutos. Em seguida, transfira para uma grade de resfriamento para esfriar completamente.
# Depois de esfriar, você pode servir o bolo simples ou decorá-lo com cobertura de sua preferência, como ganache de chocolate ou glacê de chocolate.

